/* Is this item a separator? */
static int
name_is_separator (name)
     Lisp_Object name;
{
  int isseparator = (((char *)XSTRING (name)->data)[0] == 0);

  if (!isseparator)
    {
      /* Check if name string consists of only dashes ('-') */
      char *string = (char *)XSTRING (name)->data;
      while (*string == '-') string++;
      isseparator = (*string == 0);
    }

  return isseparator;
}


